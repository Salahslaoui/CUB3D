#include "../cub3d_bonus.h"

int	ft_strshr_bonus(char *s, char c)
{
	int			i;

	i = 0;
	if (!s)
		return (0);
	while (s && s[i])
	{
		if (s[i] == c)
			return (1);
		i++;
	}
	return (0);
}
